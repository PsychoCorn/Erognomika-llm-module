from __future__ import annotations

"""
================================================================================
Файл: llm_task_schemas_ru.py
Назначение:
    Этот файл содержит все основные Pydantic-схемы, которые нужны для
    интеграции LLM в API agile-доски задач.

    Идея следующая:
    1. У нас есть внутренняя схема задачи в системе.
    2. У нас есть контекст, который мы передаем модели.
    3. У нас есть строго определенный формат ответа модели.
    4. Мы валидируем этот формат через Pydantic, чтобы:
       - не принимать неожиданные поля,
       - не ломать бизнес-логику,
       - иметь предсказуемый контракт между API и LLM.

Почему это важно:
    LLM по своей природе может "фантазировать", добавлять лишние поля,
    возвращать неверные типы, путать даты и т.д. Поэтому схемы здесь нужны
    не только для удобства, но и как защитный слой между моделью и системой.

Что именно описано в этом файле:
    - TaskStatus:
        перечисление допустимых статусов задач.
    - TaskBase:
        базовая схема задачи без id и project_name.
    - Task:
        полная схема задачи, как она хранится/передается внутри системы.
    - LLMAddTask:
        схема задачи, которую модель предлагает ДОБАВИТЬ.
    - LLMChangeTask:
        схема задачи, которую модель предлагает ИЗМЕНИТЬ.
    - ChatMessage:
        одно сообщение в истории чата.
    - LLMContext:
        контекст, который передается модели.
    - ChatRequest:
        запрос от пользователя в чатовый endpoint.
    - EncouragementRequest:
        запрос для генерации подбадривающего сообщения.
    - LLMResponse:
        строгий формат ответа модели.
    - LLMEnvelope:
        дополнительная обертка для логирования/трассировки.

Важно:
    Названия полей сделаны в snake_case, как и было запрошено.
    Это упрощает интеграцию с Python/FastAPI/Pydantic и делает контракт
    единообразным.

Совместимость:
    Код написан под Pydantic v2.
================================================================================
"""

from datetime import date, datetime, UTC
from enum import Enum
from typing import List, Optional

from pydantic import BaseModel, ConfigDict, Field


class TaskStatus(str, Enum):
    """
    Перечисление допустимых статусов задачи.

    Почему используется Enum:
        Вместо "свободной" строки мы явно задаем допустимые значения.
        Это помогает:
        - валидировать входные и выходные данные;
        - исключать опечатки;
        - держать контракт стабильным;
        - подсказывать допустимые значения через OpenAPI / JSON Schema.

    Наследование от str нужно для того, чтобы:
        1. enum красиво сериализовался в JSON как строка;
        2. FastAPI/Pydantic работали с ним максимально удобно.

    Допустимые значения:
        - TODO
        - In Progress
        - Finished

    Обрати внимание:
        Значение "In Progress" содержит пробел, потому что именно такой
        формат был указан в исходной постановке задачи.
        Внутри Python-константа называется IN_PROGRESS, а наружу в JSON
        уходит строка "In Progress".
    """

    TODO = "TODO"
    IN_PROGRESS = "In Progress"
    FINISHED = "Finished"


class TaskBase(BaseModel):
    """
    Базовая схема задачи.

    Зачем нужна отдельная базовая схема:
        Часто разные модели содержат один и тот же набор полей.
        Например:
        - задача для отображения в системе,
        - задача для добавления,
        - задача для изменения.

        Чтобы не дублировать поля по многу раз, их выносят в базовый класс.
        После этого более специфичные схемы просто наследуются от него.

    Что содержит эта схема:
        Все основные поля задачи, кроме:
        - id
        - project_name

    Почему здесь нет id:
        Потому что базовая логика задачи как сущности может использоваться
        и для новых задач, которых еще нет в БД.

    Почему здесь нет project_name:
        Потому что в постановке задачи для LLM-операций project_name
        специально не нужен в add/change-форматах.
        ProjectName есть в "полной" задаче, но не в LLM-командах.

    Примечание по датам:
        Используется тип date, а не datetime, потому что в ТЗ поля
        StartDate / EndDate указаны как date.
        Это значит, что формат ожидается примерно такой:
            "2026-03-06"

    Примечание по description:
        Пока поле обязательное, потому что в описании схемы оно указано
        как полноценная часть задачи.
        Если позже окажется, что описание может отсутствовать, можно будет
        заменить тип на Optional[str] и добавить default=None.
    """

    project_id: int = Field(
        ...,
        description="Уникальный идентификатор проекта, к которому относится задача.",
    )
    title: str = Field(
        ...,
        description="Краткий заголовок задачи. Должен в сжатом виде описывать суть.",
    )
    description: str = Field(
        ...,
        description="Подробное текстовое описание задачи.",
    )
    parent_id: Optional[int] = Field(
        default=None,
        description=(
            "ID родительской задачи. "
            "Если поле равно null/None, значит задача верхнего уровня."
        ),
    )
    start_date: date = Field(
        ...,
        description="Плановая или фактическая дата начала задачи.",
    )
    end_date: date = Field(
        ...,
        description="Плановая или целевая дата завершения задачи.",
    )
    status: TaskStatus = Field(
        ...,
        description="Текущий статус задачи из фиксированного набора допустимых значений.",
    )


class Task(TaskBase):
    """
    Полная схема задачи.

    Это уже не абстрактная/базовая форма, а полноценная задача, как ее удобно
    передавать внутри системы и в контексте модели.

    От TaskBase она отличается тем, что добавляет:
        - id
        - project_name

    Когда использовать:
        - при чтении задач из БД;
        - при передаче списка задач в контекст LLM;
        - при возврате задач наружу из API;
        - при внутренней логике, где нужна полная информация о задаче.

    Почему project_name есть здесь:
        Потому что в общей сущности задачи это полезная информация.
        Например, если одна и та же модель потенциально работает с несколькими
        проектами, название проекта может быть удобным для логов, UI и дебага.

    Почему id обязателен:
        Потому что это уже существующая задача, а не заготовка для создания.
    """

    id: int = Field(
        ...,
        description="Уникальный идентификатор задачи в системе.",
    )
    project_name: str = Field(
        ...,
        description="Человекочитаемое название проекта.",
    )


class LLMTaskBase(BaseModel):
    """
    Базовая схема задачи именно для команд, которые возвращает LLM.

    На первый взгляд может показаться, что она совпадает с TaskBase, и это
    действительно так почти полностью. Однако отдельный класс полезен по двум
    причинам:

    1. Семантическое разделение.
       TaskBase — это скорее общая схема доменной сущности.
       LLMTaskBase — это схема именно команды/предложения от модели.

    2. Будущая расширяемость.
       В будущем может понадобиться:
       - сделать некоторые поля необязательными именно для LLM,
       - добавить служебные комментарии,
       - ослабить или ужесточить валидацию именно для модельного ответа,
       при этом не ломая доменную схему TaskBase.

    Почему здесь тоже есть project_id:
        В текущей постановке задачи в add/change-формате убирается только
        project_name, но project_id сохраняется.
        Это логично: модель должна явно указывать, к какому проекту относится
        создаваемая или изменяемая задача.

    Если позже вы решите, что project_id тоже не нужен в ответах модели,
    его можно будет убрать отсюда и из наследников.
    """

    project_id: int = Field(
        ...,
        description="ID проекта, к которому относится задача, предлагаемая моделью.",
    )
    title: str = Field(
        ...,
        description="Заголовок задачи, предлагаемой моделью.",
    )
    description: str = Field(
        ...,
        description="Описание задачи, предлагаемой моделью.",
    )
    parent_id: Optional[int] = Field(
        default=None,
        description=(
            "ID родительской задачи для связи с иерархией. "
            "Если None, задача считается корневой."
        ),
    )
    start_date: date = Field(
        ...,
        description="Дата начала задачи в формате YYYY-MM-DD.",
    )
    end_date: date = Field(
        ...,
        description="Дата окончания задачи в формате YYYY-MM-DD.",
    )
    status: TaskStatus = Field(
        ...,
        description="Статус задачи, который модель должна выбрать из допустимого списка.",
    )


class LLMAddTask(LLMTaskBase):
    """
    Схема задачи для добавления.

    Это команда от модели вида:
        "нужно создать новую задачу с такими-то полями".

    Особенности:
        - id здесь НЕТ, потому что задача еще не создана в системе;
        - project_name здесь НЕТ, потому что по ТЗ оно не должно приходить
          от модели в add_tasks;
        - все остальные поля присутствуют.

    Где используется:
        в поле LLMResponse.add_tasks

    Как дальше обычно обрабатывается:
        1. API получает ответ модели.
        2. Валидирует JSON через LLMResponse.
        3. Из add_tasks берет список LLMAddTask.
        4. Для каждой записи создает реальную задачу в БД.
        5. Уже БД/сервис назначает ей настоящий id.
    """

    pass


class LLMChangeTask(LLMTaskBase):
    """
    Схема задачи для изменения.

    Это команда от модели вида:
        "нужно изменить уже существующую задачу с таким-то id".

    Отличие от LLMAddTask:
        здесь ОБЯЗАТЕЛЬНО есть id, потому что без него непонятно,
        какую именно существующую задачу нужно обновлять.

    Почему project_name отсутствует:
        По постановке задачи оно не должно быть частью change_tasks.
        Для изменения достаточно id задачи и изменяемых полей.

    Важное замечание по semantics:
        Сейчас эта схема предполагает, что модель возвращает ПОЛНОЕ новое
        состояние задачи, а не patch/partial update.
        То есть expected contract такой:
            "вот все поля задачи после изменения"

        Это удобно для строгих контрактов, но если вы позже захотите
        partial update, придется сделать отдельную схему, где поля optional.
    """

    id: int = Field(
        ...,
        description="ID существующей задачи, которую нужно изменить.",
    )


class ChatMessage(BaseModel):
    """
    Одно сообщение из истории чата.

    Эта схема специально сделана очень простой и универсальной:
        - role
        - content

    Почему этого достаточно:
        Для большинства LLM-интеграций базового chat-контекста обычно хватает.
        Например:
            {"role": "user", "content": "..."}
            {"role": "assistant", "content": "..."}
            {"role": "system", "content": "..."}

    Зачем это нужно:
        По ТЗ модели передаются последние 5 сообщений истории чата.
        Значит, история должна быть структурированной, а не просто одной строкой.

    Примечание:
        Здесь role оставлен строкой, а не Enum.
        Это сделано намеренно, чтобы не ограничивать интегратора:
        возможно, позже захочется использовать дополнительные роли:
        - tool
        - developer
        - function
        - moderator
        и т.д.

        Если хочется строгую валидацию, role позже можно заменить на Enum.
    """

    role: str = Field(
        ...,
        description="Роль сообщения, например: user, assistant, system.",
    )
    content: str = Field(
        ...,
        description="Текстовое содержимое сообщения.",
    )


class LLMContext(BaseModel):
    """
    Контекст, который передается модели.

    По текущему ТЗ модель должна получать:
        1. Историю чата (последние 5 сообщений)
        2. Список задач в проекте
        3. Текущую дату

    Именно это и encapsulate-ится в данной схеме.

    Подробно по полям:
        - chat_history:
            список сообщений чата.
            Важно: по смыслу тут должны лежать последние 5 сообщений,
            но сама схема не умеет автоматически "обрезать" историю —
            она только валидирует размер, если список уже подготовлен.
            Обычно обрезание делается до создания объекта LLMContext.

        - project_tasks:
            актуальный список задач проекта, который отправляется модели,
            чтобы она понимала текущее состояние доски.

        - current_date:
            текущая дата. Нужна модели, чтобы она могла:
            - опираться на "сегодня";
            - формировать сроки;
            - писать более уместные ответы;
            - не путаться во временном контексте.

    Почему current_date передается явно:
        Не стоит надеяться, что модель "сама знает сегодняшнюю дату".
        Лучше всегда передавать ее явно в контексте.
    """

    chat_history: List[ChatMessage] = Field(
        default_factory=list,
        description="Последние сообщения чата. По ТЗ ожидаются последние 5 сообщений.",
        max_length=5,
    )
    project_tasks: List[Task] = Field(
        default_factory=list,
        description="Полный список задач текущего проекта.",
    )
    current_date: date = Field(
        ...,
        description="Текущая дата, относительно которой модель должна рассуждать.",
    )


class ChatRequest(BaseModel):
    """
    Запрос к чатовой LLM-ручке.

    Предполагаемый сценарий:
        Пользователь отправляет новое сообщение в интерфейсе доски,
        а backend собирает:
        - само новое сообщение,
        - контекст проекта,
        - историю,
        и отправляет это в LLM-слой.

    Что хранит:
        - user_message:
            новое входящее сообщение от пользователя;
        - context:
            весь структурированный контекст для модели.

    Зачем разделять user_message и chat_history:
        Потому что текущее сообщение пользователя часто удобно передавать
        отдельно от уже существующей истории.
        Это упрощает pipeline и делает структуру запроса более явной.

    Возможная альтернатива:
        Можно было бы просто дописывать текущее user_message в chat_history
        и не хранить отдельное поле.
        Но текущий вариант обычно удобнее для backend-логики.
    """

    user_message: str = Field(
        ...,
        description="Новое сообщение пользователя, на которое должна ответить модель.",
    )
    context: LLMContext = Field(
        ...,
        description="Контекст проекта и чата для обработки пользовательского сообщения.",
    )


class EncouragementRequest(BaseModel):
    """
    Запрос для генерации автоматического подбадривающего сообщения.

    По постановке задачи:
        Подбадривающие сообщения дергаются автоматически, но условия их вызова
        не входят в текущую задачу.

    Поэтому схема здесь очень простая:
        мы просто передаем контекст, а внешняя логика уже решает,
        когда именно и почему нужно запросить encouragement message.

    Зачем отдельная схема, если внутри только context:
        1. Семантическая ясность.
           Понятно, что это отдельный тип вызова модели.
        2. Будущая расширяемость.
           Позже сюда можно добавить, например:
           - trigger_reason
           - mood_score
           - inactivity_hours
           - user_name
           и т.д.

    Иными словами:
        сейчас это почти "обертка", но полезная и правильная с точки зрения API.
    """

    context: LLMContext = Field(
        ...,
        description="Контекст проекта и чата для генерации подбадривающего сообщения.",
    )


class LLMResponse(BaseModel):
    """
    Строгий формат ответа модели.

    Это одна из самых важных схем во всем файле.
    Она задает единственный допустимый контракт ответа LLM.

    Допустимые ключи по ТЗ:
        - message_to_user
        - add_tasks
        - change_tasks
        - delete_tasks

    Почему включен model_config = ConfigDict(extra="forbid"):
        Это запрещает любые лишние поля.
        Например, если модель внезапно вернет:
            {
                "message_to_user": "...",
                "priority": "high"
            }
        то такая структура НЕ пройдет валидацию.

        Это очень полезно, потому что:
        - защищает от галлюцинаций модели;
        - не дает silently проглатывать неожиданные поля;
        - делает интеграцию надежнее.

    Семантика полей:
        - message_to_user:
            обычный текстовый ответ пользователю в чат.
            Может отсутствовать, если модель должна только предложить
            операции над задачами.

        - add_tasks:
            список новых задач, которые надо создать.

        - change_tasks:
            список существующих задач, которые надо изменить.

        - delete_tasks:
            список id задач, которые надо удалить.
            По ТЗ порядок важен:
                сначала дети, потом родители.
            Это важно для иерархических задач, чтобы не ломать внешние ключи
            и бизнес-логику при удалении.

    Почему списки по умолчанию пустые:
        Это удобнее, чем Optional[List[...]].
        Тогда вызывающему коду не нужно каждый раз делать проверки на None:
            if response.add_tasks:
                ...
        Пустой список просто означает "ничего не нужно делать".

    Почему message_to_user optional:
        Потому что есть сценарии, где модель может только предложить
        изменения на доске без отдельного текстового ответа.
    """

    model_config = ConfigDict(extra="forbid")

    message_to_user: Optional[str] = Field(
        default=None,
        description="Текстовый ответ пользователю в чат.",
    )
    add_tasks: List[LLMAddTask] = Field(
        default_factory=list,
        description="Список новых задач, которые нужно создать в системе.",
    )
    change_tasks: List[LLMChangeTask] = Field(
        default_factory=list,
        description="Список существующих задач, которые нужно изменить в системе.",
    )
    delete_tasks: List[int] = Field(
        default_factory=list,
        description=(
            "Список ID задач для удаления. "
            "Порядок важен: сначала дочерние задачи, потом родительские."
        ),
    )


class LLMEnvelope(BaseModel):
    """
    Дополнительная обертка над ответом модели.

    Эта схема не является строго обязательной по ТЗ, но практически очень
    полезна в реальной интеграции.

    Зачем она нужна:
        Иногда хочется хранить не только "полезный" ответ модели, но и
        дополнительную мета-информацию:
        - имя модели;
        - время генерации;
        - трассировку;
        - служебные данные.

    Что содержит:
        - model_name:
            имя модели, если нужно явно фиксировать, какая модель ответила;
        - created_at:
            время создания объекта-обертки;
        - response:
            сам полезный ответ в формате LLMResponse.

    Где может использоваться:
        - логирование;
        - аудит;
        - отладка;
        - кэширование;
        - хранение истории вызовов LLM.

    Примечание по времени:
        Используется timezone-aware UTC datetime.
        Это удобнее для серверной мета-информации и безопаснее, чем naive datetime.
    """

    model_name: Optional[str] = Field(
        default=None,
        description="Имя модели, которая сгенерировала ответ, например deepseek-chat.",
    )
    created_at: datetime = Field(
        default_factory=lambda: datetime.now(UTC),
        description="UTC-время создания объекта-обертки.",
    )
    response: LLMResponse = Field(
        ...,
        description="Полезная нагрузка — строго валидированный ответ модели.",
    )


